class L:
    """Локализатор. L.t('key') возвращает строку на текущем языке."""
    _lang = "en"

    @classmethod
    def set_lang(cls, lang: str):
        cls._lang = lang if lang in TRANSLATIONS else "en"

    @classmethod
    def t(cls, key: str) -> str:
        return TRANSLATIONS.get(cls._lang, TRANSLATIONS["en"]).get(
            key, TRANSLATIONS["en"].get(key, key))
